<h1><strong>2. How to adopt a pet</strong></h1>
<p style="text-align: left;">If you’ve never adopted a pet before, you may be wondering, how does the <strong><span style="color: #008000;">process</span></strong> work? How <strong><span style="color: #008000;">long</span></strong> does it take? How much does it <strong><span style="color: #008000;">cost</span></strong>? The information below will help give you an overview of how it works to adopt a pet from the two main types of organizations:&nbsp;<em>Shelters</em>&nbsp;and&nbsp;<em>Rescues</em>. Each organization is different, but it helps to understand the two main types and&nbsp;what to expect, so you can have a better overall pet adoption experience.</p>
<h2><strong>Adopting from a Shelter</strong></h2>
<p><em>Shelters</em>&nbsp;include public shelters like the city and county animal shelters, animal control, police and health departments, dog wardens, and are often called the pound.&nbsp;There are also private shelters that might use the words “humane society” or “SPCA” in their name. They are all separate, independently run organizations or government offices.&nbsp;<em>Just because shelters have similar names does not mean they are connected — just like different banks might have the word “bank” in their name.</em></p>
<p>Shelters often have a physical facility, with staff, and operating hours. Some have volunteers and do adoption events around town too. If the pet you see on MireillePetAdoption.com says that pet is at a <strong><a href="general.php">Shelter</a>&nbsp;</strong>and has an address with business hours, the easiest way to adopt that pet may be to go visit. That’s because some shelters are under-staffed and may not answer all phone calls or emails. Especially if the pet listing says ACT QUICKLY you should go to that shelter ASAP to see that pet!</p>
<p>Each shelter’s adoption process is&nbsp;<span style="color: #ff6600;">different</span>.&nbsp;Here’s an <em>example</em> of how a <strong>shelter</strong> adoption might work:</p>
<ol>
<li>You find a pet you want to adopt on <a href="index.php">MireillePetAdoption.com</a> who’s at a shelter.<span style="color: #ffffff;"><br>
</span></li>
<li>You go to the shelter and see the pet. He’s adorable! You ask at the shelter desk and they have a staff or volunteer take him out so you can meet with him. You fall in love.<span style="color: #ffffff;"><br>
</span></li>
<li>They put the pet back, and you go up to the shelter desk, give them your photo ID, pay the adoption fee (average range is $25 to $125), and get copies of the pet’s vaccination records and sterilization certificate so you can get him licensed.<span style="color: #ffffff;"><br>
</span></li>
<li>You take your new pet home!</li>
</ol>
<h2></h2>
<h2><strong>Adopting from a Rescue</strong></h2>
<p>Rescues most often have their pets in foster homes or private boarding facilities, and are run by volunteers. They may have adoption events at pet stores on the weekends. If a pet you see on Adopt-a-Pet.com says that pet is at a <a href="general.php">Rescue</a>, be sure to read the pet description and the Adoption Process under that, and follow each rescue’s instructions. Some prefer you email them first, some prefer you fill out their application before asking about a pet, so they can answer your questions more knowledgeably.</p>
<p>Please be patient when waiting for a response from a rescue, giving them a few days to respond. Rescue volunteers are people just like you and me, often with very full lives in addition to their rescue volunteering.</p>
<p>Each rescue’s adoption process is <span style="color: #ff6600;">different</span>. Here’s one <em>example</em> of how a <strong>rescue</strong> adoption might work:</p>
<ol>
<li>You find a pet you want to adopt on <a href="general.php">Adopt-a-Pet.com</a> who’s at a rescue.<span style="color: #ffffff;"><br>
</span></li>
<li>You email the rescue, who asks you to fill out their online application. The next day, one of the rescue’s volunteers calls you and you talk to them more about the pet. It seems like a good match, and agree to come meet the pet at their adoption event in a local pet store that weekend.<span style="color: #ffffff;"><br>
</span></li>
<li>You meet the pet and fall in love. An event volunteer calls a home check volunteer, and you all arrange for a home visit. After the home visit, you get a call from a volunteer to let you know you will be the home adopting the pet! They email you copies of his vaccination and sterilization records. You arrange a delivery day for your new pet.<span style="color: #ffffff;"><br>
</span></li>
<li>Your new pet is brought by his foster volunteer to your home!&nbsp;You sign their adoption contract and pay their adoption fee (usually ranges from $100 to $300 but sometimes more).</li>
</ol>
<h2><strong>Shelter vs Rescue&nbsp;</strong></h2>
<p>Some of the advantages of adopting from a <strong>shelter</strong> are you might be able to see many pets for adoption all at once at their facility. Many shelters have a very easy or no screening process, and will let you take home whatever pet you want the same day. Adoption fees are often lower than a rescue’s, but you may need to pay for additional vet care after adopting.</p>
<p>Some of the advantages of adopting from a <strong>rescue</strong> are they often know a lot about each of the pets in their care, since the pets may be in foster homes. A rescue might have a more involved screening process, which can take more time, but can help you adopt a pet that is more likely to be an easier match for your home. Adoption fees are often higher than a shelter, but often include vet care that would cost more if you paid the vet yourself.</p>
<p>We hope this overview and these examples help you better understand how you can adopt a pet. If you’re ready to start looking for a pet to adopt, click on the “Find A Pet” menu above to search for <a href="general.php">dogs for adoption</a>, <a href="general.php">cats for adoption</a>, or <a href="general.php">other pets for adoption</a> like rabbits, horses, and more.</p>
<p>Thank you for adopting a pet!</p>
